    <h1>Facilities</h1>
    <h2>Dhvanyaloka Centre For Indian Studies</h2>

    <p>Dhvanyaloka Centre for Indian Studies, provides a quiet, academic atmosphere. The centre includes living quarters, kitchen/dining area, classroom and coconut grove.</p>
    
    <h3>Accommodation</h3>
    <img src="<?=STOCK_IMAGE_URL?>accomodation.jpg" class="img-responsive banner">
    <p>Single or double rooms are of International standard and very comfortable. Computer facilities, with Internet connections are available. There is a phone utilizing Calling Card for students’ use in the Centre. Vegetarian breakfast, lunch &amp; dinner are catered and kitchen facilities are available for students’ use. In addition, Mysore has many excellent restaurants, which typically carry both vegetarian and non-vegetarian menu.</p>
    <p>The food provided at the centre is safe, healthy, and well balanced, and the drinking water is safe. Nevertheless, visitors to the centre are advised to exercise caution outside the Dhvanyaloka Centre for Indian Studies campus.</p>

    <h3>Wellness</h3>
    <p>The program has a qualified physician for advice on call at all times, and private clinics in Mysore are equipped to meet most medical emergencies. Students particularly from USA are required to purchase health insurance.</p>
    <p>Further, all the programs are inclusive of a unique wellness program based on rich Indian Vedic Traditional Ayurveda, an internationally acclaimed medicinal practice.</p>
    
    <h3>Climate</h3>
    <img src="<?=STOCK_IMAGE_URL?>climate.jpg" class="img-responsive banner">
    <p>The climate in Mysore is always pleasant and salubrious. After the monsoon rains end in September the weather is cool till February. Throughout most of the program highs will be in the 80s with lows in the 70s (Fahrenheit). By December the lows will have dropped to the 50s-60s (Fahrenheit). On the whole the humidity will be quite low.</p>
    <p>For overseas visitors to the Centre, Summer Clothing is recommended, along with a medium weight jacket or sweater for the cooler winter nights. Students should avoid shorts, short skirts, and sleeveless shirts, as these are generally considered inappropriate, particularly for women.</p>
