    <h1>Objectives</h1>
    <h2>Dhvanyaloka Centre For Indian Studies</h2>

    <p>The centre has been established for promoting the study, research and development of Indian Culture, Fine Arts, Literature, Performing and Traditional Arts, Ayurveda, Yoga, Dance and Music in India as well as in the overseas countries, which are influenced, by such Arts and Culture. 
    <ul>
        <li>The Centre has the required infrastructure for conducting Residential and Non Residential Courses.</li>
        <li>The Centre has collaborated with various Universities overseas and has catered to their needs to train and educate their student community in the fields of Indian Culture, Art, Literature, Performing and Traditional Arts, Ayurveda, Yoga, Dance and Music.</li>
        <li>The Centre has organized and conducted Academic Programs for Indian and International Institutions.</li>
        <li>The Centre will, in a phased manner establish and develop Research Centres for Advanced Studies in the field of Indian Culture, Art, Literature, Performing and Traditional Arts, Ayurveda, Yoga, Dance and Music.</li>
        <li>The Centre will exploit the opportunities available from the various European and American Universities which have been showing great interest in helping their Faculty Members and Students in understanding Indian Culture, Civilization, Gender Issues, Science and Technological Developments, by organizing Study Programs for them in India.</li>
        <li>The Centre will also reciprocate by helping Indian Students to have similar facility in those overseas Universities by organizing Programs in active collaboration with the overseas academic Institutions and Universities. </li>
    </ul>
    <p>In its pursuit the Centre will organize Seminars, Training Programs and Workshops, periodically, in the areas like Personality Development, Human Resources, Conservation of Cultural Heritage, Archeology, Epigraphy, Numismatics, Preservation of Ancient Manuscripts Indian literature and such other areas.</p>
    <p>The Centre shall also undertake and promote programs which shall enhance the prestige and glory of India.</p>
